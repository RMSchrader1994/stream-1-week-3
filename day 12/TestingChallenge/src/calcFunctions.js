function areaOfCircle(n) {
    let result = 3.1416 * (n * n);
    return Number(result.toFixed(2));
}

function squareRoot(n) {
    let result = Math.sqrt(n);
    return Number(result.toFixed(2));
}

function squareMe(n) {
    let result = n*n;
    return Number(result.toFixed(2));
}
function cubeMe(n) {
    let result = n*n*n;
    return Number(result.toFixed(2));
}
function areaOfTriangle(num1, num2) {
    let result = (num1 * num2) / 2;
    return Number(result.toFixed(2));
}